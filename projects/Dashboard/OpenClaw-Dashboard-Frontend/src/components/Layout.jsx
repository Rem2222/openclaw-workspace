export default function Layout() {
  const location = useLocation();
  const currentPath = location.pathname;
  const counts = useCounts();
  const [sidebarOpen, setSidebarOpen] = useState(false);

  const toggleSidebar = () => setSidebarOpen(!sidebarOpen);
  const closeSidebar = () => setSidebarOpen(false);

  const currentNavItem = NAV_ITEMS.find(item => currentPath === item.path);

  return (
    <div className="app-layout">
      {/* Mobile Header */}
      <header className="mobile-header">
        <button className="mobile-menu-btn" onClick={toggleSidebar}>
          <svg xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" strokeWidth={1.5} stroke="currentColor" width="24" height="24">
            <path strokeLinecap="round" strokeLinejoin="round" d="M3.75 6.75h16.5M3.75 12h16.5m-16.5 5.25h16.5" />
          </svg>
        </button>
        <span className="mobile-header-title">{currentNavItem?.label || 'Dashboard'}</span>
        <div style={{ width: 40 }}></div>
      </header>

      {/* Sidebar Overlay */}
      <div
        className={`sidebar-overlay ${sidebarOpen ? 'sidebar-overlay--visible' : ''}`}
        onClick={closeSidebar}
      />

      {/* Sidebar */}
      <aside className={`sidebar ${sidebarOpen ? 'sidebar--open' : ''}`}>
        {/* Header with Logo */}
        <div className="sidebar-header">
          <div className="sidebar-logo">OpenClaw</div>
        </div>

        {/* Navigation */}
        <nav className="sidebar-nav">
          {NAV_ITEMS.map((item) => {
            const isActive = currentPath === item.path || 
                           (currentPath === '/' && item.path === '/agents');
            const count = counts[item.countKey];
            
            return (
              <Link
                key={item.path}
                to={item.path}
                className={`nav-item ${isActive ? 'nav-item--active' : ''}`}
              >
                {item.icon}
                <span>{item.label}</span>
                {count !== null && (
                  <span style={{ marginLeft: 'auto', fontSize: '12px', opacity: 0.7 }}>
                    {item.path === '/projects' && count.open > 0 && (
                      <span style={{ marginRight: '6px' }}>
                        {count.open}/{count.total}
                      </span>
                    )}
                    {item.path === '/approvals' && count > 0 && (
                      <span style={{ 
                        display: 'inline-block', 
                        width: '6px', 
                        height: '6px', 
                        borderRadius: '50%', 
                        backgroundColor: 'var(--danger)',
                        marginRight: '6px',
                        animation: 'pulse 2s infinite'
                      }}></span>
                    )}
                    {count}
                  </span>
                )}
              </Link>
            );
          })}
        </nav>

        {/* Sidebar Footer */}
        <div className="sidebar-footer">
          <Link to="/settings" className="settings-link">
            ⚙️ Settings
          </Link>
        </div>
      </aside>

      {/* Main Content */}
      <main className="app-main">
        <Outlet />
      </main>

      {/* Status Bar */}
      <StatusBar />
    </div>
  );
}
